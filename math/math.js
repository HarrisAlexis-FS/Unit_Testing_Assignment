const addNumbers = (a, b) => {
    return a + b;
};

const subNumbers = (a, b) => {
    return a - b;
};

const multNumbers = (a, b) => {
    return a * b;
};

const divNumbers = (a,b) => {
    return a / b;
};

const sqrtNumber = (a) => {
    return Math.sqrt(a);
};

const maxNumber = (a,b) => {
    return Math.max(a,b)
};



module.exports = {addNumbers, subNumbers, multNumbers, divNumbers, sqrtNumber,maxNumber};